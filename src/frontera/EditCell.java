    private void jEditCellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditCellActionPerformed
        // TODO add your handling code here:
        int index = jListCells.getSelectedIndex();
        
        jCompany.setText(frontera.Pantalla_principal.listaCell.get(index).getCompany());
        jProcessor.setText(frontera.Pantalla_principal.listaCell.get(index).getProcessor());
        
        String Ram = Double.toString(frontera.Pantalla_principal.listaCell.get(index).getRam());
        jRam.setText(Ram);
        
        String Rom = Double.toString(frontera.Pantalla_principal.listaCell.get(index).getRom());
        jRom.setText(Rom);
        
        String Camera = Double.toString(frontera.Pantalla_principal.listaCell.get(index).getCamera());
        jCamera.setText(Camera);
        
        String Batery = Double.toString(frontera.Pantalla_principal.listaCell.get(index).getBatery());
        jBatery.setText(Batery);
        
        String Cost = Double.toString(frontera.Pantalla_principal.listaCell.get(index).getCost());
        jCost.setText(Cost);
        
        String Plan = Boolean.toString(frontera.Pantalla_principal.listaCell.get(index).isPlan());
        jPlan.setText(Plan);
        
    }//GEN-LAST:event_jEditCellActionPerformed

